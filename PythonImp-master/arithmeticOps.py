a=100;
print(a/28)#prints the result of the division a/28 in a floating point format.
print(a//28)#prints the result of the division a/28 in an integer format. It does not take the decimal part. 
print(a%28)#Modulo
print(a*28)#Mult
print(a+28)#Addition
print(a-28)#Subtraction
print(a**2)#Exponentiation, prints 100^2.

